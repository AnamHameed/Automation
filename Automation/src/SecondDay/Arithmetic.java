package SecondDay;

public class Arithmetic {

	public static void main(String[] args) {
				System.out.println("alhamdulillah");
		
				boolean a=true;
				int c=7;
				System.out.println("This is the value of variable a="+a);
				System.out.println("This is the value of variable c="+c);
		
				int i=600;
				int w=60;
				int addition=i+w;
				System.out.println("This is the value of a addition = "+addition);
				int subtraction=i-w;
				System.out.println("This is the value of a subtraction = "+subtraction);
				int division=i/w;
				System.out.println("This is the value of a division = "+division);
				int multiplication=i*w;
				System.out.println("This is the value of a multiplication = "+multiplication);
				int remainder=i%w;
				System.out.println("This is the value of a remainder = "+remainder);
						
	}

}
